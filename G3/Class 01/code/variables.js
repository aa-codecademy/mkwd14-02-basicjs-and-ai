//Numbers
let num = 7; //declaration & initialization
console.log(num); //output: 7
console.log("Type of num: ");
console.log(typeof(num)); 

//Strings

//string because we use quotes - we can use single or double quotes
let stringNum = '7'; //declaration & initialization
console.log(stringNum); //output: '7'
console.log("Type of stringNum: ");
console.log(typeof(stringNum)); 

stringNum = 5; //a variable can change type and value
console.log(stringNum);
console.log("Type of stringNum after change: ");
console.log(typeof(stringNum));

let firstName = "Ana"; //double quotes
console.log(firstName); //output: Ana
console.log("Type of firstName: ");
console.log(typeof(firstName));

firstName = 'Petko';
console.log(firstName); //output: Petko
console.log("Type of firstName after change: ");
console.log(typeof(firstName));

//Booleans
let bool = true;
console.log(bool);
console.log("Type of bool");
console.log(typeof(bool));

let secondBool = false;
console.log(secondBool);
console.log("Type of secondBool");
console.log(typeof(secondBool));

let stringBool = "true"; //this will be of type string
console.log(stringBool);
console.log("Type of stringBool");
console.log(typeof(stringBool));

//Undefined 
let n; //declartion - we have not given n a value yet
console.log(n);
console.log("Type of n");
console.log(typeof(n));
n = 6;
console.log(n);
console.log("Type of n");
console.log(typeof(n));

//Null
let nullable = null; //we give the nullable variable the value null
console.log(nullable);
console.log("Type of nullable");
console.log(typeof(nullable));

